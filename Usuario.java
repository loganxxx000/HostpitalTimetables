
public class Usuario {

	private String nombre;
	private int numViernes;
	private int numSabados;
	private int numDomingos;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getNumViernes() {
		return numViernes;
	}
	public void setNumViernes(int numViernes) {
		this.numViernes = numViernes;
	}
	public int getNumSabados() {
		return numSabados;
	}
	public void setNumSabados(int numSabados) {
		this.numSabados = numSabados;
	}
	public int getNumDomingos() {
		return numDomingos;
	}
	public void setNumDomingos(int numDomingos) {
		this.numDomingos = numDomingos;
	}
	
	
}
